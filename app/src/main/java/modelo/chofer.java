package modelo;

public class chofer {
    private String Uid, CedulaCh, NombreCo,ApellidoCh, CorreoCh , TelefonoCho;
    private String Genero;

    public chofer() {

    }

    public String getUid() {
        return Uid;
    }

    public void setUid(String uid) {
        Uid = uid;
    }

    public String getCedulaCh() {
        return CedulaCh;
    }

    public void setCedulaCh(String cedulaCh) {
        CedulaCh = cedulaCh;
    }

    public String getNombreCo() {
        return NombreCo;
    }

    public void setNombreCo(String nombreCo) {
        NombreCo = nombreCo;
    }

    public String getApellidoCh() {
        return ApellidoCh;
    }

    public void setApellidoCh(String apellidoCh) {
        ApellidoCh = apellidoCh;
    }

    public String getCorreoCh() {
        return CorreoCh;
    }

    public void setCorreoCh(String correoCh) {
        CorreoCh = correoCh;
    }

    public String getTelefonoCho() {
        return TelefonoCho;
    }

    public void setTelefonoCho(String telefonoCho) {
        TelefonoCho = telefonoCho;
    }

    public String getGenero() {
        return Genero;
    }

    public void setGenero(String genero) {
        Genero = genero;
    }
}
